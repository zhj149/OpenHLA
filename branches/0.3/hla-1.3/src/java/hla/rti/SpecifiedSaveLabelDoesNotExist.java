package hla.rti;

public final class SpecifiedSaveLabelDoesNotExist
  extends RTIexception
{
  public SpecifiedSaveLabelDoesNotExist()
  {
  }

  public SpecifiedSaveLabelDoesNotExist(String message)
  {
    super(message);
  }

  public SpecifiedSaveLabelDoesNotExist(String message, Throwable cause)
  {
    super(message, cause);
  }

  public SpecifiedSaveLabelDoesNotExist(Throwable cause)
  {
    super(cause);
  }

  public SpecifiedSaveLabelDoesNotExist(String message, int serial)
  {
    super(message, serial);
  }

  public SpecifiedSaveLabelDoesNotExist(String message, Throwable cause,
                                        int serial)
  {
    super(message, cause, serial);
  }

  public SpecifiedSaveLabelDoesNotExist(Throwable cause, int serial)
  {
    super(cause, serial);
  }
}
